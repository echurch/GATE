#include <Cluster.h>

ClassImp(gate::Cluster);

//=======================================================
gate::Cluster::Cluster() : Hit(){
//=======================================================
}

//=======================================================
gate::Cluster::~Cluster(){ 
//=======================================================
  
  if (_wform) delete _wform; 
}

//=======================================================
void gate::Cluster::Info(std::ostream& s) const{
//=======================================================

    s << "========= Cluster instance ======="<< std::endl;
    
    Hit::Info(s);

    s << "Number of hits: " << this->GetHits().size()<< std::endl;
    
    s << "=================================="<< std::endl;

}

//=======================================================
std::ostream& operator << (std::ostream& s, const gate::Cluster& c) {
//=======================================================

    c.Info(s);
  
    return s; 
}
