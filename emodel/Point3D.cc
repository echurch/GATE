#include<Point3D.h>

ClassImp(gate::Point3D);

//=======================================================
gate::Point3D::Point3D(double x, double y, double z) : _x(x),_y(y),_z(z){
//=======================================================
}


//=======================================================
void gate::Point3D::Info(ostream& s) const{
//=======================================================
    
    s << " Point3D: ( " << _x <<", "<<_y<<", "<<_z<< " )"<<std::endl;

}

//=======================================================
ostream& operator << (ostream& s, const gate::Point3D& p) {
//=======================================================

    p.Info(s);
  
    return s; 
}
