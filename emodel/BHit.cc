
#include <BHit.h>

ClassImp(gate::BHit);

//=======================================================
gate::BHit::BHit() : BObject(),_pos(),_amp(0), _time(-1){}
//=======================================================


//=======================================================
gate::BHit::~BHit(){} 
//=======================================================


   
//=======================================================
void gate::BHit::Info(std::ostream& s) const{
//=======================================================

    BObject::Info(s);
    
    _pos.Info(s);

    s << "Hit amplitdue: " << this->GetAmplitude()<< std::endl;
    
    s << "Hit time: " << this->GetTime()<< std::endl;
        
}

//=======================================================
std::ostream& operator << (std::ostream& s, const gate::BHit& bhit) {
//=======================================================

    bhit.Info(s);
  
    return s; 
}
